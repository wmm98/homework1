i = 0
sum = 0
num = 0
while i < 64:
    num = 2 ** i
    sum += num
    i += 1
print(sum)  # 输出总数
m2 = (sum * 50 / 1000000000)  # 输出吨数
print("%.2ft"%(m2))

# 吨转为克
m1 = m2 * 1000000
# 500克就体积55毫升，那m1多少毫升？
ML = (m1 / 500) * 550
# 毫升转为升
L = ML / 1000
# 转为立方米
M = L/1000

h = M / 7140
print("%.2fm"%(h))  # 输出高度
