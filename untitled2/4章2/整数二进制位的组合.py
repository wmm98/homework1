# z = x + y
# print(z)


num1, num2 = input().split()
# 十进制转二进制并且取除了0b的子串,补全为16位，前面补0
num1T = bin(int(num1))[2:].rjust(16, "0")

print(bin(int(num1)))
# 0b1101001000
print(num1T)
# 0000001101001000
print(num1T[:8])
# 00000011

num2T = bin(int(num2))[2:].rjust(16, "0")
print(num2T[:8])
# 00001000
result = num1T[:8]+num2T[:8]


print(result)
# 0000001100001000
print(int(result, 2))  # 二进制转为十进制