#例3-1-1  定义列表
#字符串列表
courses = ['Python程序设计',  '计算机原理',  '操作系统',  '数据结构',  '算法设计',  '软件工程导论']
print(courses)

stuff = ['菠菜', '大蒜子', '排骨', '冬瓜', '葡萄', '鸡蛋', '生菜']
print(stuff)

#数字列表
scores = [99.5, 100, 97.5]
print(scores)

#列表元素的类型可以多样
ns = ['python', 10000, 77.5, '加油']
print(ns)

#空列表
blist = []
print(blist)

#引用变量的列表
sun = 'sun'
liu = 'liu'
yang = 'yang'
xm = [sun, liu, yang]
print(xm)

one_elem = ['a']   #列表只有一个元素
print(one_elem)
