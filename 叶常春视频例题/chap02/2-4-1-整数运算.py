#例2-4-1  整数运算
print("2+4=", 2 + 4)
print("5-3=", 5 - 3)
print("3*3=", 3 * 3)
print("9/2=", 9/2)    #结果是浮点数（即实数）
print("9//2=", 9//2)  #整除，商为结果，去掉余数
print("9**2=", 9**2)  #幂次运算。x**y,求x的y次方
print("9%2=", 9%2)    #求余

a = 3   #整数值存为一个对象。变量a引用了该对象。
b = 4
r = a * a + b * b  #变量r引用的对象存储了a, b的平方和。
print(r)