'''【问题描述】编写一个程序，将某个位数不确定的正整数进行三位分节后输出。
【输入形式】用户在第一行输入一个正整数。
【输出形式】程序将这个正整数三位分节输出，每隔三位加一个逗号（英文逗号）分割。

【样例输入】1234567
【样例输出】1,234,567
【样例说明】用户输入正整数1234567，程序从个位开始每隔三位加一个逗号（英文逗号）分割，所以输出为1,234,567

'''

n = input()
m = [i for i in n]
# print(m)
m1 = m[::-1]
# print(m1)
# ['7', '6', '5', '4', '3', '2', '1']

j = 3
while len(m1) > 3:
    m1.insert(j, ",")
    j += 4
    if j >= len(m1):
        break

for r in m1[::-1]:
    print(r, end="")