'''
【问题描述】给出一个不多于5位的正整数，要求：
1.求出它是几位数。
2.分别打印出每一位数字。
3.按照逆序打印出每一位数字。
【输入形式】控制台输入该正整数。
【输出形式】控制台分行输出输出结果。
【样例1输入】
1234
【样例1输出】
4
1234
4321
【样例1说明】1234位数为4，分别为1，2，3，4，逆序为4321
【样例2输入】
200
【样例2输出】
3
200
002
【样例1说明】200位数为3，分别为2，0，0，逆序为002
'''

n = input()
if len(n) <= 5:
    print(len(n))
    print(n)
    print(n[::-1])